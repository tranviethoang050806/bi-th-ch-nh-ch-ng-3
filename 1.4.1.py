def sum(a,b):
  print("sum = "+str(a+b))
sum(4,5)#tinh tong 2 so 4,5
sum(3,7)#tinh tong 2 so 3,7

# Giải thích:
# def sum(a, b): → định nghĩa hàm tên là sum có 2 tham số a và b.
# Trong hàm: cộng a + b, chuyển kết quả sang chuỗi bằng str(), rồi in ra.
# sum(4, 5) → in ra sum = 9
# sum(3, 7) → in ra sum = 10
