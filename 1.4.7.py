def checkValue(n):
    if n % 2 ==0:
        print("Đây là một số chẵn")
    else:
        print("Đây là một số lẻ")
# Gọi hàm để kiểm tra
checkValue(7)
checkValue(10)

# Giải thích:
# Hàm nhận 1 số n.
# Toán tử % là phép chia lấy dư.
# n % 2 == 0 → số chẵn.
# ngược lại → số lẻ.
# In ra thông báo tương ứng.
